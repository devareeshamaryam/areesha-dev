export default function TechStack() {
  const [angle, setAngle] = useState(0);
  const [paused, setPaused] = useState(false);
  const rafRef = useRef(null);
  const lastTimeRef = useRef(null);
  const angleRef = useRef(0);

  useEffect(() => {
    const speed = 0.3; // degrees per ms * 16 ≈ ~18deg/sec

    const tick = (time) => {
      if (!paused) {
        if (lastTimeRef.current !== null) {
          const delta = time - lastTimeRef.current;
          angleRef.current = (angleRef.current + speed * (delta / 16)) % 360;
          setAngle(angleRef.current);
        }
        lastTimeRef.current = time;
      } else {
        lastTimeRef.current = null;
      }
      rafRef.current = requestAnimationFrame(tick);
    };

    rafRef.current = requestAnimationFrame(tick);
    return () => cancelAnimationFrame(rafRef.current);
  }, [paused]);

  const count = techs.length;
  const radius = 170;

  return (
    <section className="bg-white py-20 px-6 overflow-hidden">
      {/* Heading */}
      <div className="text-center mb-12">
        <h2
          className="text-4xl md:text-5xl font-black text-black tracking-tight"
          style={{ fontFamily: "'Playfair Display', serif" }}
        >
          Tech Stack<span className="text-rose-300">.</span>
        </h2>
        <div className="mt-4 flex items-center justify-center gap-3">
          <div className="h-px w-12 bg-rose-200" />
          <div className="h-2 w-2 rounded-full bg-rose-300" />
          <div className="h-px w-6 bg-rose-100" />
        </div>
      </div>

      {/* Orbit */}
      <div className="flex items-center justify-center">
        <div
          className="relative"
          style={{ width: radius * 2 + 80, height: radius * 2 + 80 }}
          onMouseEnter={() => setPaused(true)}
          onMouseLeave={() => setPaused(false)}
        >
          {/* Outer dashed orbit ring */}
          <svg
            className="absolute inset-0 w-full h-full pointer-events-none"
            viewBox={`0 0 ${radius * 2 + 80} ${radius * 2 + 80}`}
          >
            <circle
              cx={(radius * 2 + 80) / 2}
              cy={(radius * 2 + 80) / 2}
              r={radius}
              fill="none"
              stroke="#fecdd3"
              strokeWidth="1.5"
              strokeDasharray="6 6"
            />
            {/* Inner decorative ring */}
            <circle
              cx={(radius * 2 + 80) / 2}
              cy={(radius * 2 + 80) / 2}
              r={radius * 0.45}
              fill="none"
              stroke="#fce7f3"
              strokeWidth="1"
              strokeDasharray="3 8"
            />
          </svg>

          {/* Center logo */}
          <div
            className="absolute flex flex-col items-center justify-center"
            style={{
              left: "50%",
              top: "50%",
              transform: "translate(-50%, -50%)",
              width: 90,
              height: 90,
            }}
          >
            <div className="w-20 h-20 rounded-full bg-rose-50 border-2 border-rose-200 flex items-center justify-center shadow-md">
              <span
                className="text-black text-xs font-bold tracking-wide text-center leading-tight"
                style={{ fontFamily: "'Playfair Display', serif" }}
              >
                Tech<br/>Stack
              </span>
            </div>
          </div>

          {/* Orbiting icons */}
          {techs.map((tech, i) => {
            const theta = ((i / count) * 360 + angle) * (Math.PI / 180);
            const cx = (radius * 2 + 80) / 2 + radius * Math.cos(theta);
            const cy = (radius * 2 + 80) / 2 + radius * Math.sin(theta);

            return (
              <div
                key={tech.name}
                className="absolute group"
                style={{
                  left: cx,
                  top: cy,
                  transform: "translate(-50%, -50%)",
                  zIndex: 10,
                }}
              >
                <div className="relative w-14 h-14 rounded-full bg-white border-2 border-rose-100 shadow-md shadow-rose-100/60 flex items-center justify-center transition-all duration-300 group-hover:scale-125 group-hover:border-rose-300 group-hover:shadow-rose-200/80 cursor-pointer">
                  {tech.svg}
                  {/* Tooltip */}
                  <div className="absolute -top-9 left-1/2 -translate-x-1/2 bg-black text-white text-xs px-2.5 py-1 rounded-full opacity-0 group-hover:opacity-100 transition-opacity duration-200 whitespace-nowrap pointer-events-none"
                    style={{ fontFamily: "'Lora', serif" }}
                  >
                    {tech.name}
                  </div>
                </div>
              </div>
            );
          })}
        </div>
      </div>


    </section>
  );
}